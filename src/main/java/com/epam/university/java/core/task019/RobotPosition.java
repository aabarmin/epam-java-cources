/**
 * Robot position.
 */
public interface RobotPosition {
    /**
     * Get x value.
     * @return value
     */
    int getX();

    /**
     * Get y value.
     * @return value
     */
    int getY();

    /**
     * Set x value.
     * @param x value
     */
    void setX(int x);

    /**
     * Set y value.
     * @param y value
     */
    void setY(int y);
}

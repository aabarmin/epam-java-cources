package com.epam.university.java.core.task019;

/**
 * Robot position.
 */
public interface RobotPosition {
    /**
     * Get x value.
     * @return value
     */
    int getPointX();

    /**
     * Get y value.
     * @return value
     */
    int getPointY();

    /**
     * Set pointX value.
     * @param pointX value
     */
    void setPointX(int pointX);

    /**
     * Set pointY value.
     * @param pointY value
     */
    void setPointY(int pointY);
}

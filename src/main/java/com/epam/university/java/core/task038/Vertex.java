package com.epam.university.java.core.task038;

/**
 * Stores data about vertex.
 */
public interface Vertex {

    /**
     * Get id of vertex.
     * @return id value
     */
    int getId();

    /**
     * Get x coordinate of vertex.
     * @return x coordinate value
     */
    int getX();

    /**
     * Get y coordinate of vertex.
     * @return y coordinate value
     */
    int getY();
}

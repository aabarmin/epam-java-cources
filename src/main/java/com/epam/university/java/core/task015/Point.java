package com.epam.university.java.core.task015;

/**
 * Point in 2-dimensional area.
 */
public interface Point {
    /**
     * Get x value of point.
     * @return value
     */
    double getX();

    /**
     * Get y value of point.
     * @return value
     */
    double getY();

    /**
     * Set x value of point.
     * @param x value
     */
    void setX(double x);

    /**
     * Set y value of point.
     * @param y value
     */
    void setY(double y);
}
